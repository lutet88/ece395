uint16_t ads131m04_rreg(uint8_t reg) {
    uint8_t cmd[3];
    reg = reg & 0x3f;
    cmd[0] = 0x0A | (reg >> 5);
    cmd[1] = (reg >> 1) & 0xf;
    cmd[2] = (reg | 0x1) << 3;
    uint8_t rcv[36];
    ads131m04_cmd(cmd, rcv, 3);
    return (rcv[18] << 8) + rcv[19];
}
